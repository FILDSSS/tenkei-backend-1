const Joi = require('joi');

const tm_processSchema = Joi.object({
    Process_CD: Joi.string().required(),
    Change_CD: Joi.string(),
    ProcessG_CD: Joi.string(),
    ResourceG_CD: Joi.string().allow(null).optional(),
    ManageG_CD: Joi.string().allow(null).optional(),
    Manhour_Calc: Joi.string().allow(null).optional(),
    Days_Calc: Joi.string().allow(null).optional(),
    Process_Name: Joi.string().allow(null).optional(),
    Process_Abb: Joi.string().allow(null).optional(),
    Process_Symbol: Joi.string().allow(null).optional(),
    Process_Mark: Joi.string().allow(null).optional(),
    Use: Joi.boolean().allow(null).optional(),
    For_Plan: Joi.boolean().allow(null).optional(),
    For_Info: Joi.boolean().allow(null).optional(),
    Graph: Joi.boolean().allow(null).optional(),
    List: Joi.boolean().allow(null).optional(),
    Outside_On: Joi.boolean().allow(null).optional(),
    Outside_Off: Joi.boolean().allow(null).optional(),
    End: Joi.boolean().allow(null).optional(),
    Coefficient: Joi.number().allow(null).optional(),
    M_Coefficient: Joi.number().allow(null).optional(),
    P_Coefficient: Joi.number().allow(null).optional(),
    Before: Joi.number().allow(null).optional(),
    After: Joi.number().allow(null).optional(),
    Operation_Time: Joi.number().integer().allow(null).optional(),
    Std_M_CAT: Joi.number().integer().allow(null).optional(),
    Std_M_Time: Joi.number().integer().allow(null).optional(),
    Std_P_CAT: Joi.number().integer().allow(null).optional(),
    Std_P_Time: Joi.number().integer().allow(null).optional(),
    T_Type: Joi.string().allow(null).optional(),
    P_Type: Joi.string().allow(null).optional(),
    S_Type: Joi.string().allow(null).optional(),
    Process_Remark: Joi.string().allow(null).optional(),
});

module.exports = { tm_processSchema };
